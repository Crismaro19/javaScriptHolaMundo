let a = 10;

// operadores de relacion relacionales
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

// operadores de relacion de igualdad
console.log( a == 10); // valida solo valor
console.log( a != 10); // valida solo valor 
console.log( a == '10');
console.log( a === '10'); // valida valor y tipo
console.log( a !== '10'); // valida valor y tipo



